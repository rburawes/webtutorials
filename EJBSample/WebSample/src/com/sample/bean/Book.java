package com.sample.bean;

public class Book {
	
	private String name;
	
	private int edition;

	public String getName() {
		return name;
	}

	public void setName( String name ) {
		this.name = name;
	}

	public int getEdition() {
		return edition;
	}

	public void setEdition( int edition ) {
		this.edition = edition;
	}
	
}
